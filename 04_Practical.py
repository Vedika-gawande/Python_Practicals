# Arithmetic Operators
a=10
b=5
print("Add : ", a+b)
print("Sub : ", a-b)
print("Mul : ", a*b)
print("Rem : ", a%b)
print("Quo : ", a/b)

# Comparision Operators

print("Less than : " , a < b)
print("Greater than : " , a > b)
print("Less than or Equal: " , a <= b)
print("Greater than or Equal: " , a>=b)
print("Equal To  : " , a==b)
print("Not Equal to  : " , a!=b)

# Assignment Operators

print("Value of a : ",a)
