# Logical 

print("AND : " , 5>3 and 4>2)
print("OR : " , 5>3 or 1>3)
print("NOT : " , not(5>3))

# Bitwise

print("Left Shift : " , a<<1)
print("Right Shift : " , a>>1)
print("Bitwise and  : " , a&b)
print("Bitwise or : " , a^1)
print("Not : " , ~a)

