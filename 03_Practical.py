# Variables and datatypes in Python
# Variables
name="vedika"
age=19
cgpa=9.0
isStudent=True
address=None

print("Value : " , name , " Type : " , type(name)) #class str
print("Value : " , age , " Type : " , type(age)) #class int
print("Value : " , cgpa , " Type : " , type(cgpa)) #class float
print("Value : " , isStudent , " Type : " , type(isStudent)) #class bool
print("Value : " , address , " Type : " , type(address)) # Class NoneType

# Datatypes
marks=[58,52,48,41] # List

student={               #dictionary
    "name":"vedika",
    "age":19,
    "rollno":12
}

subjects=("DMS","DBMS","PIP") #Tuple

unique_id={101,102,103} #set

print("Value : " , marks , " Type : " , type(marks))
print("Value : " , student , " Type : " , type(student))
print("Value : " , subjects , " Type : " , type(subjects))
print("Value : " , unique_id , " Type : " , type(unique_id))